public class ifElse {
    
    public static void main(String[] args){
        int numero = 5;

        int teste = 5;

        if (numero > teste) {
            System.out.println(numero + " é maior que " + teste);
        }
        else if (numero == 5){
            System.out.println(numero + " é igual a " + teste);
        }
        else {
            System.out.println(numero + " é menor que " + teste);
        }

    }


}
